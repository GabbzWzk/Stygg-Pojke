end

function SubOptions()

    if currentConfig ~= "Subtlety CoRe" then
    	currentConfig = "Subtlety CoRe"
        ClearConfig();
        thisConfig = 0
        -- Title
        CreateNewTitle(thisConfig,"Subtlety |cffFF0000CoRe");

        -- General Configs
        CreateGeneralsConfig();


        WrapsManager();
    end
end
